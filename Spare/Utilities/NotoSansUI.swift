import UIKit

struct NotoSansUI {
    
    static func bold(_ size: CGFloat) -> UIFont {
        return UIFont(name: "NotoSansUI-Bold", size: size)!
    }
    
    static func regular(_ size: CGFloat) -> UIFont {
        return UIFont(name: "NotoSansUI", size: size)!
    }
    
}
